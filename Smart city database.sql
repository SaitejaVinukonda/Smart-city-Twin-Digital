-- Create database
CREATE DATABASE smart_city_db;
USE smart_city_db;

-- Create tables
CREATE TABLE sensors (
    id INT PRIMARY KEY AUTO_INCREMENT,
    sensor_id VARCHAR(50) UNIQUE NOT NULL,
    sensor_type ENUM('temperature', 'traffic', 'energy', 'air_quality', 'noise') NOT NULL,
    location_name VARCHAR(100),
    latitude DECIMAL(10, 8),
    longitude DECIMAL(11, 8),
    status ENUM('active', 'inactive', 'maintenance') DEFAULT 'active',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
);

CREATE TABLE sensor_readings (
    id INT PRIMARY KEY AUTO_INCREMENT,
    sensor_id VARCHAR(50),
    value DECIMAL(10, 4),
    unit VARCHAR(20),
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    quality_score DECIMAL(3, 2) DEFAULT 1.00,
    FOREIGN KEY (sensor_id) REFERENCES sensors(sensor_id) ON DELETE CASCADE,
    INDEX idx_sensor_timestamp (sensor_id, timestamp),
    INDEX idx_timestamp (timestamp)
);

CREATE TABLE traffic_data (
    id INT PRIMARY KEY AUTO_INCREMENT,
    intersection_id VARCHAR(50),
    vehicle_count INT,
    average_speed DECIMAL(5, 2),
    congestion_level ENUM('low', 'medium', 'high') DEFAULT 'low',
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_intersection_timestamp (intersection_id, timestamp)
);


CREATE TABLE energy_consumption (
    id INT PRIMARY KEY AUTO_INCREMENT,
    district_id VARCHAR(50),
    consumption_mw DECIMAL(10, 4),
    renewable_percentage DECIMAL(5, 2),
    peak_demand DECIMAL(10, 4),
    timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    INDEX idx_district_timestamp (district_id, timestamp)
);

CREATE TABLE ai_insights (
    id INT PRIMARY KEY AUTO_INCREMENT,
    insight_type ENUM('traffic', 'energy', 'environment', 'maintenance', 'emergency'),
    title VARCHAR(200),
    description TEXT,
    confidence_score DECIMAL(3, 2),
    priority ENUM('low', 'medium', 'high', 'critical'),
    status ENUM('new', 'reviewed', 'implemented', 'dismissed') DEFAULT 'new',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    expires_at TIMESTAMP,
    INDEX idx_type_status (insight_type, status),
    INDEX idx_priority_created (priority, created_at)
);
